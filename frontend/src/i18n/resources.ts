export function menuKeyByHref(href: string) {
  if (href === "/") return "menu.home";
  if (href === "/o-nas") return "menu.about";
  if (href === "/portfolio") return "menu.portfolio";
  if (href === "/services") return "menu.services";
  if (href === "/blog") return "menu.blog";
  if (href === "/kontakty") return "menu.contacts";
  if (href === "/nagrady-i-diplomy") return "menu.awards";
  if (href === "/akcii-i-skidki") return "menu.promos";
  if (href === "/novosti") return "menu.news";
  if (href === "/otzyvy-o-nas") return "menu.reviews";
  if (href === "/karera") return "menu.career";
  if (href === "/partneram") return "menu.partners";
  if (href === "/politika-konfidencialnosti") return "menu.privacy";
  if (href === "/user/agreement") return "menu.agreement";
  return null;
}

export function siteLocaleFromLanguage(language?: string): SiteLocale {
  return language?.startsWith("en") ? "en" : "ru";
}

export function localizedValue(
  locale: SiteLocale,
  ru?: string | null,
  en?: string | null,
  fallback?: string | null,
) {
  if (locale === "en") {
    return en || fallback || ru || "";
  }

  return ru || fallback || en || "";
}
